    <h1>What is ITT?</h1>

 The simple definition for ITT(Information Tracking Tool) is a “Defect Tracking Tool”. And also it can be used to record the details about the project, Track and Resolve Issues, Bugs, Defects, Improvements and New Feature Requests.


 <h1 >Main Concepts of ITT:</h1>
 <ul>
     <li>Everything starts from creating a new project.</li>
    <li>One project can be anything, such as a new application, a report instance from staff, Orbits upgrade plan, software upgrade etc.</li>
    <li>Each project can have many issues.</li>
    <li>All documentations can be attached into the issues. One project named as “Project Documentation” is the home of documentations.</li>
    <li>If you think about project as a container, then issues can be considered as the items in the container.</li>
 </ul>

<h1>There are Three User Roles in ITT:</h1>
<ul>
    <li>Reporter – The person who creates the project or the issue.</li>
    <li>Assignee – The person who works on the issue. The assignee is assigned by the reporter. The assignee can be reporter self or anyone else.</li>
    <li>Watcher – The person who has the interest to know the progress of the project/issue. Any ITT users can assign the watcher. You can assign yourself as watchers if you are interested with some projects or issues, or assign someone else.</li>
</ul>

